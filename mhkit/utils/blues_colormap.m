function cm_data = blues_colormap(m)
% Colormap similar to matplotlib blues
cm = [
    [0.9686274509803922, 0.9843137254901961, 1.0],
    [0.8705882352941177, 0.9215686274509803, 0.9686274509803922],
    [0.7764705882352941, 0.8588235294117647, 0.9372549019607843],
    [0.6196078431372549, 0.7921568627450981, 0.8823529411764706],
    [0.4196078431372549, 0.6823529411764706, 0.8392156862745098],
    [0.2588235294117647, 0.5725490196078431, 0.7764705882352941],
    [0.12941176470588237, 0.44313725490196076, 0.7098039215686275],
    [0.03137254901960784, 0.3176470588235294, 0.6117647058823529],
    [0.03137254901960784, 0.18823529411764706, 0.4196078431372549]
];

if nargin < 1
    cm_data = cm;
else
    hsv = rgb2hsv(cm);
    cm_data = interp1(linspace(0,1,size(cm,1)),hsv,linspace(0,1,m));
    cm_data = hsv2rgb(cm_data);
end
end
